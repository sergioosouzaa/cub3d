#include "libft.h"

void	ft_bzero(void *a, size_t n)
{
	int				i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)a;
	while (n > 0)
	{
		str[i] = '\0';
		i++;
		n--;
	}
}
